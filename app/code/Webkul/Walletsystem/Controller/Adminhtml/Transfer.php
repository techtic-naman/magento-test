namespace Webkul\Walletsystem\Controller\Adminhtml;

use Magento\Backend\App\Action;

/**
 * Webkul Walletsystem Abstract class
 */
abstract class Transfer extends \Magento\Backend\App\Action
{
    /**
     * Check for is allowed
     *
     * @return boolean
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('Webkul_Walletsystem::walletpayee');
    }
}
