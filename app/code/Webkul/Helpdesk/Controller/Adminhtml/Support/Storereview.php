namespace Webkul\Helpdesk\Controller\Adminhtml\Support;

use Magento\Backend\App\Action;

class Storereview extends Action
{
    /**
     * Support Userguide Link.
     */
    public function execute()
    {
        $resultRedirect = $this->resultRedirectFactory->create();
        $resultRedirect->setUrl("https://store.webkul.com/magento2-helpdesk-extension.html#reviews");
        return $resultRedirect;
    }

    /**
     * Check Permission.
     *
     * @return bool
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('Webkul_Helpdesk::helpdesk_reviews');
    }
}
